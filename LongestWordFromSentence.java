package com.services;

public class LongestWordFromSentence {
	public void getLargestWord(String[] words) {
		int max = 0;
		String res = "";
		for (int i = 0; i < max; i++) {
			max = words[i].length();
			res = words[i];
		}
		// return res;
		System.out.println("The largest word is:" + res);
	}

}
